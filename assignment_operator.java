class assignment_operator {
    public static void main(String [] args){
        int x=8;
        int y=10;
        System.out.println(x+=y);
        System.out.println(x-=y);
        System.out.println(x*=y);
        System.out.println(x/=y);
        System.out.println(x%=y);
        System.out.println(x&=y);
        System.out.println(x|=y);
        System.out.println(x^=y);
        System.out.println(x>>=y);
        System.out.println(x<<=2);
        

        
        
    } //Java is a statically-typed language. 
    //This means that variables must be declared
    // with a specific data type and the type of a variable cannot change once it has been declared.
    
}
