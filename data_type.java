class DataType{
    public static void main(String [] args){
        int a=12;
        float f=3.3f;
        double d = 3.5d;
        char c = 'D';
        boolean yes= true;
        String my = "hello";
        long l=45;
        short s=23;

        System.out.println(a);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(yes);
        System.out.println(my);
        System.out.println(l);
        System.out.println(s);
    }
}


  